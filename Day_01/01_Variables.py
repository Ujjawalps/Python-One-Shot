"""
Variables in Python are used to store data values. 
A variable is created by assigning a value to a name using the '=' operator.
Variables do not need explicit declaration to reserve memory space.
The type of the variable is determined by the value assigned to it.
"""

# Example of variable assignment
name = "Ujjawal"      # String variable
age = 25            # Integer variable
height = 5.7        # Float variable
is_student = True   # Boolean variable

# Printing variable values
print("Name:", name)
print("Age:", age)
print("Height:", height)

if is_student:
    print(name, "is a student.")
else:
    print(name, "is not a student.")